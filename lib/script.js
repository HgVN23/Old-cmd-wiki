function dropDown() {
    var toggler = document.getElementsByClassName("drop_icon");
    var i;

    for (i = 0; i < toggler.length; i++) {
        toggler[i].addEventListener("click", drop);
    }
}
function drop() {
    this.classList.toggle("drop_icon_down");
    this.parentElement.querySelector(".drop_hide").classList.toggle("drop_show");
}